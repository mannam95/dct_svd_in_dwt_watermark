def keygen(size, x=0.01, r=3.915):
    """
    This function generates a key for the specified number of length.

    :param size: the number of keys to be generated.
    :param x: x is the initial condition.
    :param r: r is the controlled parameter.
    :return: Returs the generated keys.
    """ 
    key = []

    for i in range(size):
        x = r * x * (1-x) # Logistic map
        key.append(int((x * pow(10, 16)%2)))
    
    return key

def generate_2d_key(xdims=32, ydims=32,  x=0.01, r=3.915):
    """
    This function generates a key which will be used for watermark encryption.

    :param xdims: x axis shape
    :param ydims: y axis shape
    :param x: x is the initial condition.
    :param r: r is the controlled parameter.
    :return: Returs the generated logistic chaotic key.
    """ 

    keys = np.asarray(keygen(xdims*ydims, x, r))

    return np.resize(keys, xdims*ydims).reshape(xdims,ydims)

def encrypt_watermark(watermark_img: np.ndarray, encryption_key: np.ndarray):
    """
    This function does watermark encryption by performing an XOR operation.

    :param watermark_img: The watermark that should go through encryption.
    :param encryption_key: The encryption key.
    :return: Returs the encrypted watermark.
    """ 

    return np.bitwise_xor(watermark_img, encryption_key)

def decrypt_watermark(encypted_img: np.ndarray, encryption_key: np.ndarray):
    """
    This function does watermark encryption by performing an XOR operation.

    :param watermark_img: The watermark image that has been ecrypted.
    :param encryption_key: The encryption key.
    :return: Returs the encrypted watermark.
    """ 

    return np.bitwise_xor(encypted_img.astype(int), encryption_key.astype(int))

